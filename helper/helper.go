package helper

// multiple
func Multiplication(firstNumber float32, secondNumber float32) float32 {
	results := firstNumber * secondNumber
	return results
}

// devided
func Divison(firstNumber float32, secondNumber float32) float32 {
	results := firstNumber / secondNumber
	return results
}

// sum
func Summation(firstNumber float64, secondNumber float64) float64 {
	results := firstNumber + secondNumber
	return results
}

func Subtraction(firstNumber float32, secondNumber float32) float32 {
	results := firstNumber - secondNumber
	return results
}
